package Usual_API;

public class Runtime {
    /*
        代表程序所在的运行环境
        Runtime是一个单例类

        常见方法：
        Runtime getRuntime()    返回当前java应用程序关联的运行时对象
        void exit(int status)   终止当前运行的虚拟机
        int availableProcessors()   返回java虚拟机可用处理器数
        long totalMemory()  返回java虚拟机中的内存总量
        long freeMemory()   返回Java虚拟机中可用内存
        Process exec(String command)    启动某个程序，并返回代表程序的对象
     */
}
